func TestGoFpdfGenerator_GenerateTournamentReport_FullBracket(t *testing.T) {
	gen := pdf.NewGoFpdfGenerator()

	pA1 := &player.Player{ID: "pA1", FirstName: "A1", LastName: "Alpha", Gender: "M", SinglesElo: 1600}
	pA2 := &player.Player{ID: "pA2", FirstName: "A2", LastName: "Alpha", Gender: "M", SinglesElo: 1500}
	pA3 := &player.Player{ID: "pA3", FirstName: "A3", LastName: "Alpha", Gender: "M", SinglesElo: 1400}
	pB1 := &player.Player{ID: "pB1", FirstName: "B1", LastName: "Beta", Gender: "M", SinglesElo: 1550}
	pB2 := &player.Player{ID: "pB2", FirstName: "B2", LastName: "Beta", Gender: "M", SinglesElo: 1450}
	pB3 := &player.Player{ID: "pB3", FirstName: "B3", LastName: "Beta", Gender: "M", SinglesElo: 1350}

	win3 := []event.MatchSet{{Number: 1, ScoreA: 11, ScoreB: 5}, {Number: 2, ScoreA: 11, ScoreB: 5}, {Number: 3, ScoreA: 11, ScoreB: 5}}

	groupMatches := []event.Match{
		{ID: "gA1", Stage: "group", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pA1}, TeamB: []*player.Player{pA2}, Sets: win3},
		{ID: "gA2", Stage: "group", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pA1}, TeamB: []*player.Player{pA3}, Sets: win3},
		{ID: "gA3", Stage: "group", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pA2}, TeamB: []*player.Player{pA3}, Sets: win3},
		{ID: "gB1", Stage: "group", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pB1}, TeamB: []*player.Player{pB2}, Sets: win3},
		{ID: "gB2", Stage: "group", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pB1}, TeamB: []*player.Player{pB3}, Sets: win3},
		{ID: "gB3", Stage: "group", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pB2}, TeamB: []*player.Player{pB3}, Sets: win3},
	}

	// Knockout seeds resolve (per getITTFKnockoutSeeds) to [pA1, pB1, pA2, pB2],
	// paired via the standard bracket arrangement as (pA1 vs pB2) and (pA2 vs pB1).
	tableNum := 3
	updated := time.Now()
	knockoutMatches := []event.Match{
		{ID: "sf1", Stage: "semifinal", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pA1}, TeamB: []*player.Player{pB2}, TableNumber: &tableNum, UpdatedAt: &updated, Sets: win3},
		{ID: "sf2", Stage: "semifinal", Status: "finished", WinnerTeam: "A", TeamA: []*player.Player{pA2}, TeamB: []*player.Player{pB1}, Sets: win3},
		{ID: "final1", Stage: "final", Status: "finished", DivisionID: "div1", WinnerTeam: "A", TeamA: []*player.Player{pA1}, TeamB: []*player.Player{pA2}, Sets: win3},
	}

	var allMatches []event.Match
	allMatches = append(allMatches, groupMatches...)
	allMatches = append(allMatches, knockoutMatches...)

	maxElo := int16(1700)
	divs := []*division.Division{
		{ID: "div1", Name: "Elite Division", MinElo: 1000, MaxElo: &maxElo, Category: "singles"},
		{ID: "div-mismatch", Name: "Doubles Only", MinElo: 0, MaxElo: nil, Category: "doubles"},
		{ID: "div-skip", Name: "No Division", MinElo: 0, MaxElo: nil, Category: "both"},
	}

	ev := &event.Event{
		ID:             "e1",
		Name:           "Full Bracket Championship",
		Type:           "singles",
		Format:         "groups_elimination",
		Status:         "finished",
		StartDate:      time.Now(),
		GroupPassCount: 2,
		Participants:   []*player.Player{pA1, pA2, pA3, pB1, pB2, pB3},
		Groups: []event.Group{
			{ID: "gA", Name: "Elite - Group A", Players: []*player.Player{pA1, pA2, pA3}},
			{ID: "gB", Name: "Elite - Group B", Players: []*player.Player{pB1, pB2, pB3}},
		},
		Matches: allMatches,
		Metrics: &event.TournamentMetrics{
			TotalMatchesPlayed:    9,
			TotalSetsPlayed:       27,
			TotalPointsScored:     500,
			AveragePointsPerMatch: 55.5,
			AverageSetsPerMatch:   3,
			CleanSweeps:           9,
			AverageEloAtStart:     1500,
			DivisionMetrics: map[string]event.DivisionMetric{
				"div1":    {TotalMatchesPlayed: 3, AveragePointsPerMatch: 55},
				"default": {TotalMatchesPlayed: 6, AveragePointsPerMatch: 56},
			},
		},
	}

	pdfBytes, err := gen.GenerateTournamentReport(ev, divs)
	if err != nil {
		t.Fatalf("unexpected error generating tournament report: %v", err)
	}
	if len(pdfBytes) == 0 {
		t.Fatal("expected non-empty byte slice for PDF report")
	}
	if !bytes.HasPrefix(pdfBytes, []byte("%PDF")) {
		t.Error("expected generated bytes to start with %PDF header")
	}
}

func TestGetDivisionPlaces(t *testing.T) {
	p1 := &player.Player{ID: "p1", FirstName: "Winner", LastName: "User"}
	p2 := &player.Player{ID: "p2", FirstName: "Runner", LastName: "Up"}
	p3 := &player.Player{ID: "p3", FirstName: "Semi", LastName: "Finalist"}

	ev := &event.Event{
		Status: "finished",
		Format: "elimination",
		Type:   "singles",
		Matches: []event.Match{
			{
				Stage:      "final",
				Status:     "finished",
				DivisionID: "div1",
				TeamA:      []*player.Player{p1},
				TeamB:      []*player.Player{p2},
				WinnerTeam: "A",
			},
			{
				Stage:      "semifinal",
				Status:     "finished",
				DivisionID: "div1",
				TeamA:      []*player.Player{p2},
				TeamB:      []*player.Player{p3},
				WinnerTeam: "A",
			},
		},
	}

	first, second, third := pdf.GetDivisionPlaces(ev, "div1", []*player.Player{p1, p2, p3})
	if first == "" || second == "" {
		t.Errorf("expected non-empty 1st and 2nd places, got first=%q, second=%q", first, second)
	}
	if third == "" {
		t.Errorf("expected non-empty 3rd place, got %q", third)
	}
}

