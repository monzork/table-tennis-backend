func TestGetDivisionPlaces_WinnerBBranch(t *testing.T) {
	p1 := &player.Player{ID: "p1", FirstName: "Winner", LastName: "User"}
	p2 := &player.Player{ID: "p2", FirstName: "Runner", LastName: "Up"}
	p3 := &player.Player{ID: "p3", FirstName: "Semi", LastName: "Finalist"}
	p4 := &player.Player{ID: "p4", FirstName: "Other", LastName: "Semi"}

	ev := &event.Event{
		Status: "finished",
		Format: "elimination",
		Type:   "singles",
		Matches: []event.Match{
			{
				Stage:      "final",
				Status:     "finished",
				DivisionID: "div1",
				TeamA:      []*player.Player{p2}, // loser is TeamA this time
				TeamB:      []*player.Player{p1}, // winner is TeamB
				WinnerTeam: "B",
			},
			{
				Stage:      "semifinal",
				Status:     "finished",
				DivisionID: "div1",
				TeamA:      []*player.Player{p3},
				TeamB:      []*player.Player{p2},
				WinnerTeam: "B", // TeamA (p3) is the semifinal loser
			},
			{
				Stage:      "semifinal",
				Status:     "finished",
				DivisionID: "div1",
				TeamA:      []*player.Player{p1},
				TeamB:      []*player.Player{p4},
				WinnerTeam: "A", // TeamB (p4) is the semifinal loser
			},
		},
	}

	first, second, third := pdf.GetDivisionPlaces(ev, "div1", []*player.Player{p1, p2, p3, p4})
	if first != "Winner User" {
		t.Errorf("expected first to be Winner User, got %q", first)
	}
	if second != "Runner Up" {
		t.Errorf("expected second to be Runner Up, got %q", second)
	}
	if third == "" {
		t.Errorf("expected non-empty third place, got %q", third)
	}
}

