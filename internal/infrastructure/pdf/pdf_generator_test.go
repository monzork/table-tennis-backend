func TestGoFpdfGenerator_GenerateTournamentReport(t *testing.T) {
	gen := pdf.NewGoFpdfGenerator()
	if gen == nil {
		t.Fatal("expected NewGoFpdfGenerator to return non-nil instance")
	}

	p1 := &player.Player{ID: "p1", FirstName: "John", LastName: "Doe", Gender: "M", SinglesElo: 1500}
	p2 := &player.Player{ID: "p2", FirstName: "Jane", LastName: "Smith", Gender: "F", SinglesElo: 1400}
	p3 := &player.Player{ID: "p3", FirstName: "Bob", LastName: "Johnson", Gender: "M", SinglesElo: 1300}
	p4 := &player.Player{ID: "p4", FirstName: "Alice", LastName: "Williams", Gender: "F", SinglesElo: 1200}

	maxElo1500 := int16(1500)
	div1 := &division.Division{
		ID:       "div1",
		Name:     "Division 1",
		MinElo:   1000,
		MaxElo:   &maxElo1500,
		Category: "singles",
	}

	ev := &event.Event{
		ID:           "e1",
		Name:         "Championship 2026",
		Type:         "singles",
		Format:       "groups_elimination",
		Status:       "finished",
		StartDate:    time.Now(),
		Participants: []*player.Player{p1, p2, p3, p4},
		Groups: []event.Group{
			{
				ID:      "g1",
				Name:    "Group A",
				Players: []*player.Player{p1, p2},
			},
			{
				ID:      "g2",
				Name:    "Group B",
				Players: []*player.Player{p3, p4},
			},
		},
		Matches: []event.Match{
			{
				ID:         "m1",
				Stage:      "group",
				Status:     "finished",
				TeamA:      []*player.Player{p1},
				TeamB:      []*player.Player{p2},
				WinnerTeam: "A",
				Sets: []event.MatchSet{
					{Number: 1, ScoreA: 11, ScoreB: 8},
					{Number: 2, ScoreA: 11, ScoreB: 9},
					{Number: 3, ScoreA: 11, ScoreB: 5},
				},
			},
			{
				ID:         "m2",
				Stage:      "final",
				Status:     "finished",
				DivisionID: "div1",
				TeamA:      []*player.Player{p1},
				TeamB:      []*player.Player{p3},
				WinnerTeam: "A",
				Sets: []event.MatchSet{
					{Number: 1, ScoreA: 11, ScoreB: 9},
					{Number: 2, ScoreA: 11, ScoreB: 7},
					{Number: 3, ScoreA: 11, ScoreB: 8},
				},
			},
		},
		Metrics: &event.TournamentMetrics{
			TotalMatchesPlayed: 2,
			TotalSetsPlayed:    6,
			TotalPointsScored:  100,
		},
	}

	pdfBytes, err := gen.GenerateTournamentReport(ev, []*division.Division{div1})
	if err != nil {
		t.Fatalf("unexpected error generating tournament report: %v", err)
	}

	if len(pdfBytes) == 0 {
		t.Fatal("expected non-empty byte slice for PDF report")
	}

	if !bytes.HasPrefix(pdfBytes, []byte("%PDF")) {
		t.Error("expected generated bytes to start with %PDF header")
	}
}

